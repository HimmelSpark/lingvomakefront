<template>
  <v-app dark>
    <v-navigation-drawer
      app
      temporary
      v-model="drawer"
      touchless
      v-if="this.$route.path.indexOf('android') === -1">
      <v-list>
        <v-list-tile
          @click=""
          v-for="link of links"
          :key="link.title"
          :to="link.url">
          <v-list-tile-action>
            <v-icon>{{ link.icon }}</v-icon>
          </v-list-tile-action>

          <v-list-tile-content>
            <v-list-tile-title v-text="link.title"></v-list-tile-title>
          </v-list-tile-content>
        </v-list-tile>

        <v-list-tile
            v-if="isUserLoggedIn"
            @click="logout">
          <v-list-tile-action>
            <v-icon>exit_to_app</v-icon>
          </v-list-tile-action>

          <v-list-tile-content>
            <v-list-tile-title>Log Out</v-list-tile-title>
          </v-list-tile-content>

        </v-list-tile>

      </v-list>
    </v-navigation-drawer>

    <v-toolbar
      app
      dark
      v-if="this.$route.path.indexOf('mobile') === -1">
      <v-toolbar-side-icon
        @click="drawer = !drawer"
        class="hidden-md-and-up">
      </v-toolbar-side-icon>
      <v-icon @click="doPaskhalka">toys</v-icon>
      <v-toolbar-title>Lingvomake</v-toolbar-title>
      <v-spacer></v-spacer>
      <transition name="fade">
        <v-toolbar-items class="hidden-sm-and-down" v-if="renderPermission">
          <v-btn
            flat
            v-for="link in links"
            :key="link.title"
            :to="link.url">
            <v-icon left>{{ link.icon }}</v-icon>
            {{ link.title }}
          </v-btn>

          <v-divider
            class="mx-1"
            inset
            vertical>
          </v-divider>

          <v-btn
            v-if="isUserLoggedIn"
            flat
            @click="logout">
            <v-icon left>exit_to_app</v-icon>
            Log Out
          </v-btn>
        </v-toolbar-items>
      </transition>
    </v-toolbar>

    <v-content>
      <router-view></router-view>
    </v-content>

    <v-footer
        height="auto">

      <v-layout
          justify-center
          row
          wrap>
        <v-flex
            lighten-2
            py-3
            text-xs-center
            white--text
            xs12>
          &copy;2018 — <strong>RHA Team</strong>
        </v-flex>
      </v-layout>
    </v-footer>
  </v-app>
</template>

<script>



  export default {
  data() {
    return {
      paskhalka: 0,
      drawer: false
    };
  },
  computed: {
    error() {},
    isUserLoggedIn() {
      return this.$store.getters.isUserLoggedIn;
    },
    renderPermission() {
      return this.$store.getters.renderPermission;
    },
    links() {
      if (this.isUserLoggedIn) {
        return [
          { title: "Courses", icon: "work", url: "/courses" },
          { title: "Students", icon: "accessibility", url: "/students" },
		      { title: "Manage", icon: "settings", url: "/manage/application" },
        ];
      } else {
        return [
          { title: "Login", icon: "lock", url: "/login" },
          { title: "Registration", icon: "face", url: "/registration" }
        ];
      }
    }
  },
  methods: {
    doPaskhalka() {
      this.paskhalka++;
      if (this.paskhalka === 20) {
        this.paskhalka = 0;
        alert("ЪАЪ! Ты у меня дотыкаешься!");
      }
    },
    logout() {
      this.$store.dispatch("logout")
          .then(() => {
            this.$router.push('/login');
          });
    }
  }
};
</script>
